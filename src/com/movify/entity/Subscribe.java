package com.movify.entity;
public class Subscribe{
	private Integer sid; // 用户订阅id
	private Integer suser; // 订阅人外键
	private Integer sto_user; // 被订阅人外键
	public Subscribe(){}
	public Subscribe(Integer sid,Integer suser,Integer sto_user){
		this.sid=sid;
		this.suser=suser;
		this.sto_user=sto_user;
	}
	public void setSid(Integer sid){
		this.sid=sid;
	}
	public Integer getSid(){
		return sid;
	}
	public void setSuser(Integer suser){
		this.suser=suser;
	}
	public Integer getSuser(){
		return suser;
	}
	public void setSto_user(Integer sto_user){
		this.sto_user=sto_user;
	}
	public Integer getSto_user(){
		return sto_user;
	}
}