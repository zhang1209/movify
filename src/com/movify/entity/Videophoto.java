package com.movify.entity;
public class Videophoto{
	private Integer vpid; // 视频介绍图id
	private String vpname; // 视频介绍图路径
	private Integer video; // 视频外键
	public Videophoto(){}
	public Videophoto(Integer vpid,String vpname,Integer video){
		this.vpid=vpid;
		this.vpname=vpname;
		this.video=video;
	}
	public void setVpid(Integer vpid){
		this.vpid=vpid;
	}
	public Integer getVpid(){
		return vpid;
	}
	public void setVpname(String vpname){
		this.vpname=vpname;
	}
	public String getVpname(){
		return vpname;
	}
	public void setVideo(Integer video){
		this.video=video;
	}
	public Integer getVideo(){
		return video;
	}
}